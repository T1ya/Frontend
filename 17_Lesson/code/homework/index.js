function formatString(input) {
    // –í–∞—à–∞ —Ä–µ–∞–ª–∏–∑–∞—Ü–∏—è –∑–¥–µ—Å—å
    return input.replace(/([0-9]{4})\.([0-9]{3})\.([0-9]{3})\s([0-9]{2})/,`$1 $2:$3-$4`)
}


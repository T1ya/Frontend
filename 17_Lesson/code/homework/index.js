// –ó–∞—Ç–µ–º –æ–±—Ä–∞–±–æ—Ç–∞–π—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Å –ø–æ–º–æ—â—å—é .then() –∏ .catch().

const randPromise = new Promise(function(resolve,reject) {
    let val = Math.random() < 0.5;
    setTimeout(() => {
        if (val) {
           resolve("Best day of my life")
        } else {
            reject(new Error("Something is off"))
        }
    }, 300);
})

randPromise
    .then((day) =>{
        console.log(day)
    })
    .catch((err)=>{
        console.log(err)
    })